interface IPadrao 
{
    /* Interface é utilizada para criar exclusivamente metodos obrigatorios
        Uma classe pode implementar varias interfaces, diferente da classe abstrata
        Nao contem calculos, condicinais, laços, etc, isso acontece na classe que a implementa
    */
    void somar(int n1, int n2);

    void subtrair(int n1, int n2);

}